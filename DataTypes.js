/* Data Types:
undefined: A variable that hasn't been given a value yet.
null: When something is intentionally empty or doesn't exist.
boolean: A true or false value, like a yes or no answer.
string: A bunch of characters, like words or sentences.
symbol: A unique name or identifier, like a special keyword.
number: A value that's a number, like 1, 2, or 3.
object: A collection of information, like a box that holds lots of things.
*/
var undefinedVar; // undefined
var age = 18; // number
var myName = "Ali"; // string
var isMale = true; // boolean
var salary = null; // null
var stuff = {}; // object

console.log(typeof undefinedVar);
console.log(typeof age);
console.log(typeof myName);
console.log(typeof isMale);
console.log(typeof salary);
console.log(typeof stuff);
