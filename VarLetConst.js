// The difference between var, let, and const

// var: Global variable, can be changed anywhere.
// let: Local variable, can be changed within a block.
// const: Constant variable, cannot be changed once set.

var myName = "Ali";
let myAge = 18;
const myGender = "Male";

console.log(myName);
console.log(myAge);
console.log(myGender);
