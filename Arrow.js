// Arrow functions in JavaScript are a concise way to write functions.
// Here are some examples of arrow functions:

// Example 1: A simple arrow function that takes two parameters and returns their sum
let add = (a, b) => a + b;

// Example 2: An arrow function that takes no parameters and returns a string
let greet = () => "Hello, world!";

// Example 3: An arrow function that takes an array and returns a new array with each element doubled
let double = arr => arr.map(num => num * 2);

// Example 4: An arrow function that takes a string and returns the string with all letters converted to lowercase
let toLower = str => str.toLowerCase();


